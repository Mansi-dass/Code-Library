#include <stdio.h>

int main(int argc, char const *argv[])
{
    int a = 5;
    float b = 8.3;
    char c = 'u';
    int d = 45;

    printf("The value of a is %d \n", a);
    printf("The value of a is %f \n", b);
    printf("The value of a is %c \n", c);
    printf("The value of a is %d \n", a + d);
    return 0;
}